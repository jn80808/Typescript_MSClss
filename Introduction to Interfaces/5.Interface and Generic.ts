// Let's create an interface named `Automobile`.

// This interface describes the structure of an automobile object.

// It includes the following properties:

// * `type` – the type of automobile, such as `"car"` or `"motorcycle"`.
// * `brand` – the manufacturer or brand, such as `"Ferrari"`.
// * `colors` – an array of available colors, for example `["black", "red"]`.
// * `description` – a string that provides additional details about the automobile.



interface Automobile {
    type: string;
    brand: string;
    colors: string[];
    description: string;

}







